    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $notif = Notification::create($request->all());
            $this->sendToFlutter($notif);
            return response(json_encode(["message" => "Notification bien envoyée", "type" => "success"]), 200);
        } catch (\Throwable $th) {
            return response(json_encode(["message" => $th->getMessage(), "type" => "error"]), 500);
        }
    }

    public function sendToFlutter(Notification $notification)
    {
        try {
            broadcast(new NewNotificationSent($notification))->toOthers();
        } catch (\Throwable $th) {
            throw $th;
        }
    }

